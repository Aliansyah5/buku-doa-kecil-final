// Fungsi untuk mendapatkan ayat random
export const getRandomVerse = () => {
  const randomIndex = Math.floor(Math.random() * inspirationalVerses.length);
  return inspirationalVerses[randomIndex];
};

// Fungsi untuk mendapatkan ayat berdasarkan tanggal (konsisten per hari)
export const getDailyVerse = () => {
  const today = new Date();
  const dayOfYear = Math.floor(
    (today - new Date(today.getFullYear(), 0, 0)) / 1000 / 60 / 60 / 24
  );
  const index = dayOfYear % inspirationalVerses.length;
  return inspirationalVerses[index];
};
